let num1 = prompt("what is your first number")
let num2 = prompt("what is your second number")

let operation = ("what operation are you using?")

if (operation = "+") {
    addition(num1 + num2)
}
if (operation = "-") {
    subtraction(num1 - num2)
}
if (operation = "/") {
    division(num1 / num2)
}
if (operation = "x") {
    multiplication(num1 * num2)
}